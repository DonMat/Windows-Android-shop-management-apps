package com.io.sklep.Adapters.Produkt;


public class Produkt {
	String nazwa;
	double cena;
	int ilosc;
	int kategoria;
	int id;
	public Produkt(String nazwa, double cena, int ilosc, int kategoria, int id) {
		super();
		this.nazwa = nazwa;
		this.cena = cena;
		this.ilosc = ilosc;
		this.kategoria = kategoria;
		this.id = id;
	}
		
}
